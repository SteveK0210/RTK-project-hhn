/**
 * @brief  The application entry point.
 * @retval int
 */
int main(void)
{
	/* USER CODE BEGIN 1 */
	/* Status of api are returned to this variable */
	int8_t rslt = 0;
	uint8_t status = 0;

	/* Resolve to exit the loop */
	uint8_t restart = 0;

	/* Variable to store sensor time value */
	uint32_t sensor_time;

	/* USER CODE END 1 */

	/* MCU Configuration--------------------------------------------------------*/

	/* Reset of all peripherals, Initializes the Flash interface and the Systick. */
	HAL_Init();

	/* USER CODE BEGIN Init */

	/* USER CODE END Init */

	/* Configure the system clock */
	SystemClock_Config();

	/* Configure the peripherals common clocks */
	PeriphCommonClock_Config();

	/* USER CODE BEGIN SysInit */

	/* USER CODE END SysInit */

	/* Initialize all configured peripherals */
	MX_GPIO_Init();
	MX_SPI2_Init();
	/* USER CODE BEGIN 2 */
	rslt = bmi08x_interface_init(&bmi08xdev, BMI08X_SPI_INTF, BMI088_VARIANT);

	while (!restart)
	{
		rslt = bmi08a_perform_selftest(&bmi08xdev);

		if (rslt == BMI08X_OK) rslt = bmi08g_perform_selftest(&bmi08xdev);
		else restart = 0;

		if (rslt == BMI08X_OK) rslt = init_bmi08x(&bmi08xdev);
		else restart = 0;

		if (rslt == BMI08X_OK) rslt = enable_bmi08x_data_synchronization_interrupt(&bmi08xdev);
		else restart = 0;

		if (rslt == BMI08X_OK) restart = 1;
	}

	/* USER CODE END 2 */

	/* Infinite loop */
	/* USER CODE BEGIN WHILE */
	while (1)
	{
		previous_time = sensor_time * BMI08X_SENSORTIME_RESOLUTION;

#ifdef	GET_ARRAY
		for (int i = 0; i < SIZE; i++){
			rslt = bmi08a_get_synchronized_data(&bmi08x_accel[i], &bmi08x_gyro[i], &bmi08xdev);
		}
#else
		rslt = bmi08a_get_synchronized_data(&bmi08x_accel, &bmi08x_gyro, &bmi08xdev);
#endif
		rslt = bmi08a_get_sensor_time(&bmi08xdev, &sensor_time);
		elapsed_time = sensor_time * BMI08X_SENSORTIME_RESOLUTION;
		duration = (elapsed_time - previous_time) * 1000;

		HAL_Delay(1000);
		/* USER CODE END WHILE */

		/* USER CODE BEGIN 3 */
	}
	/* USER CODE END 3 */
}

